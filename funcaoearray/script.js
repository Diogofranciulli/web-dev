const pessoas = ['fulano', 'ciclano', 'beltrano']

// add um elemento ao final do array
pessoas.push("diogo")

//remove o ultimo elemento do array
pessoas.pop()

// add um elemento no inicio do array
pessoas.unshift('katia')

// remove o primeiro elemento do array
pessoas.shift()

// atualizar um elemento do array
pessoas[1] = "caio"

console.log(pessoas)
